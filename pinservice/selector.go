type NodeSelector struct {
	nodes []string
	cid   string
}

func (n *NodeSelector) Len() int {
	return len(n.nodes)
}

func (n *NodeSelector) Less(i, j int) bool {
	cidEnc := sha256.Sum256([]byte(n.cid))
	iEnc := sha256.Sum256([]byte(n.nodes[i]))
	jEnc := sha256.Sum256([]byte(n.nodes[j]))

	var iSum, jSum = 0, 0
	for index := 0; index < len(cidEnc); index++ {
		iSum += bitsinbyte[^(cidEnc[index] ^ iEnc[index])]
		jSum += bitsinbyte[^(cidEnc[index] ^ jEnc[index])]
	}
	if iSum > jSum {
		return true
	}
	return false
}

func (n *NodeSelector) Swap(i, j int) {
	n.nodes[i], n.nodes[j] = n.nodes[j], n.nodes[i]
}
